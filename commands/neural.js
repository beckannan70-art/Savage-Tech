const axios = require('axios');

module.exports = {
  name: 'neural',
  category: 'ai',
  description: 'Chat with Intel\'s NeuralChat',
  async execute(sock, msg, args) {
    const query = args.join(' ');
    if (!query) return sock.sendMessage(msg.key.remoteJid, { text: '❓ Ask NeuralChat?' });
    try {
      const url = `https://apis.xwolf.space/api/ai/neural?q=${encodeURIComponent(query)}`;
      const res = await axios.get(url);
      const reply = res.data.status ? (res.data.result || 'No response') : `⚠️ ${res.data.error}`;
      await sock.sendMessage(msg.key.remoteJid, { text: `🤖 *NeuralChat:*\n${reply.slice(0, 2000)}` });
    } catch { await sock.sendMessage(msg.key.remoteJid, { text: '❌ API error' }); }
  }
};
